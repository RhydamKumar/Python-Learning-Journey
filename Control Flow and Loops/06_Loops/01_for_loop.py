for i in range(1,6):
    print (i)   # prints numbers from 1 to (6-1) = 5 (increasing order)


for i in range(2,101,2): #(start, stop, step)
    print(i) # prints even numbers from 2 to (11-1) = 10

for i in range(1,10,2):
    print(i) # prints odd numbers from 1 to (10-1) = 9

for i in range(5,0,-1):
    print(i) # prints numbers from 5 to 1 in decreasing order
    