name = "Rhydam2004"

print (name[1:5]) # goes from index 1 to 5-1=4  
print (name[:5]) # goes from starting index to 5-1=4
print (name[5:]) # goes from 5 to the end of the string


print (name[0:8:2]) #skips one character 
print (name[0:8:3]) #skips two characters
print (name[::2]) #skips one character from the whole string
print (name[::3]) #skips two characters from the whole string


