a = 20
b = "27"
c = 15
g = 18.576

# String to integer
d = int(b)
print(d)  # Output: 27
print(type(d))  # Output: <class 'int'>

# Integer to string
e = str(a)
print(e)  # Output: "20"
print(type(e))  # Output: <class 'str'>

# Integer to float
f = float(c)
print(f)  # Output: 15.0
print(type(f))  # Output: <class 'float'>

# Float to integer
h = int(g)
print(h)  # Output: 18
print(type(h))  # Output: <class 'int'>