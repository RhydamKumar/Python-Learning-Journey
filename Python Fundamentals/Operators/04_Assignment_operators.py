a = 49

# Using assignment operators
a += 1
print(a)  # After addition Output: 50
a -= 2
print(a)  # After subtraction Output: 48
a *= 2
print(a)  # After multiplication Output: 96
a /= 4
print(a)  # After division Output: 24.0
a //= 5
print(a)  # After floor division Output: 4.0
a %= 3
print(a)  # After modulus Output: 1.0
a **= 4
print(a)  # After exponentiation Output: 1.0